# conversao de tipos, coercao
# type convertion, typecasting, coercion
# e o ato de converter um tipo em outro
# tipos imutaveis e primitivos:
# str, int, floar, bool
print(int('1') + int('1')) # coercao de uma str para int para realizar a soma.
print(float('1') + 1) # coercao de uma str para float para realizar a soma.
print(bool('')) # uma str vazia e considerada False em bool.
print(bool(' ')) # uma str com conteudo, ate mesmo um espaco em branco  e considerada True.
print(str(11) + 'b') # coercao de um int para str para concatenacao de string.
